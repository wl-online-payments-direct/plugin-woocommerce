<?php

declare(strict_types=1);

// phpcs:disable Inpsyde.CodeQuality.LineLength.TooLong

use Syde\Vendor\Worldline\Dhii\Services\Factories\Alias;
use Syde\Vendor\Worldline\Dhii\Services\Factories\Constructor;
use Syde\Vendor\Worldline\Dhii\Services\Factory;
use Syde\Vendor\Worldline\Dhii\Services\Service;
use Syde\Vendor\Worldline\Inpsyde\PaymentGateway\DefaultIconsRenderer;
use Syde\Vendor\Worldline\Inpsyde\PaymentGateway\Icon;
use Syde\Vendor\Worldline\Inpsyde\PaymentGateway\IconProviderInterface;
use Syde\Vendor\Worldline\Inpsyde\PaymentGateway\StaticIconProvider;
use Syde\Vendor\Worldline\Inpsyde\WorldlineForWoocommerce\PayPalGateway\PayPalGatewayModule;
use Syde\Vendor\Worldline\Inpsyde\WorldlineForWoocommerce\PayPalGateway\Payment\PayPalRequestModifier;
use Syde\Vendor\Worldline\Inpsyde\WorldlineForWoocommerce\Vaulting\WcTokenRepository;
use Syde\Vendor\Worldline\Inpsyde\WorldlineForWoocommerce\WorldlinePaymentGateway\Api\HostedCheckoutUrlFactory;
use Syde\Vendor\Worldline\Inpsyde\WorldlineForWoocommerce\WorldlinePaymentGateway\Api\WcOrderBasedOrderFactoryInterface;
use Syde\Vendor\Worldline\Inpsyde\WorldlineForWoocommerce\WorldlinePaymentGateway\GatewayIds;
use Syde\Vendor\Worldline\Inpsyde\WorldlineForWoocommerce\WorldlinePaymentGateway\Payment\HostedPaymentProcessor;

return static function (): array {
    $moduleRoot = dirname(__FILE__, 2);

    $gatewayId = GatewayIds::PAYPAL;

    $supportedCurrencies = [
        'AUD', 'BRL', 'CAD', 'CZK', 'DKK', 'EUR', 'HKD', 'HUF', 'ILS', 'JPY',
        'MYR', 'MXN', 'TWD', 'NZD', 'PHP', 'NOK', 'PLN', 'GBP', 'RUB', 'SGD',
        'CHF', 'SEK', 'THB', 'USD',
    ];

    return [
        "payment_gateway.$gatewayId.form_fields" =>
            Service::fromFile("$moduleRoot/inc/fields.php"),

        "payment_gateway.$gatewayId.method_title" => static fn (): string =>
        __('PayPal (Worldline)', 'worldline-for-woocommerce'),
        "payment_gateway.$gatewayId.title" => static fn () =>
        __('PayPal', 'worldline-for-woocommerce'),
        "payment_gateway.$gatewayId.method_description" => static fn (): string =>
        __(
            'Accept payments with PayPal.',
            'worldline-for-woocommerce'
        ),
        "payment_gateway.$gatewayId.description" => static fn (): string => '',
        "payment_gateway.$gatewayId.order_button_text" => static fn (): ?string => null,
        "payment_gateway.$gatewayId.payment_request_validator" =>
            new Alias('payment_gateways.noop_payment_request_validator'),
        "payment_gateway.$gatewayId.payment_processor" => new Factory(
            [
                'worldline_payment_gateway.hosted_checkout_url_factory',
                'worldline_payment_gateway.wc_order_factory',
                'vaulting.repository.wc.tokens.' . GatewayIds::HOSTED_CHECKOUT,
                'worldline_payment_gateway.hosted_checkout_language',
                'paypal.request_modifier',
            ],
            static function (
                HostedCheckoutUrlFactory $hostedCheckoutUrlFactory,
                WcOrderBasedOrderFactoryInterface $wcOrderBasedOrderFactory,
                WcTokenRepository $wcTokenRepository,
                ?string $hostedCheckoutLanguage,
                PayPalRequestModifier $payPalRequestModifier
            ): HostedPaymentProcessor {
                return new HostedPaymentProcessor(
                    $hostedCheckoutUrlFactory,
                    $wcOrderBasedOrderFactory,
                    $wcTokenRepository,
                    $hostedCheckoutLanguage,
                    $payPalRequestModifier
                );
            }
        ),
        "payment_gateway.$gatewayId.supports" => static function (): array {
            return [
                'products',
                'refunds',
            ];
        },
        "payment_gateway.$gatewayId.refund_processor" => new Alias('payment_gateway.' . GatewayIds::HOSTED_CHECKOUT . '.refund_processor'),
        "payment_gateway.$gatewayId.availability_callback" => static function () use ($supportedCurrencies): callable {
            return static function () use ($supportedCurrencies): bool {
                $currentCurrency = \get_woocommerce_currency();

                return \in_array($currentCurrency, $supportedCurrencies, true);
            };
        },
        "payment_gateway.$gatewayId.method_icon_provider" => new Factory([
            'assets.get_module_static_asset_url',
        ], static function (callable $getStaticAssetUrl): IconProviderInterface {
            /** @var string $src */
            $src = $getStaticAssetUrl(
                PayPalGatewayModule::PACKAGE_NAME,
                "images/paypal-logo.svg"
            );

            $icon = new Icon('paypal-logo', $src, 'PayPal logo');

            return new StaticIconProvider($icon);
        }),
        "payment_gateway.$gatewayId.gateway_icons_renderer" => new Constructor(
            DefaultIconsRenderer::class,
            ["payment_gateway.$gatewayId.method_icon_provider"]
        ),
        "paypal.request_modifier" => new Constructor(
            PayPalRequestModifier::class,
            []
        ),
    ];
};
